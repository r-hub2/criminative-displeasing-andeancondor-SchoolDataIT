year.patternB  <- function(Year){
  Year <- as.character(Year)
  pattern <- dplyr::case_when(
    Year %in% c("2016", "201516", "2015/16", "2015/2016")  ~ c("201516","2016"),
    Year %in% c("2018", "201718", "2017/18", "2017/2018")  ~ "201718",
    Year %in% c("2019", "201819", "2018/19", "2018/2019")  ~ "201819",
    Year %in% c("2021", "202021", "2020/21", "2020/2021")  ~ "2021202021",
    Year %in% c("2022", "202122", "2021/22", "2021/2022")  ~ "2021202122",
    Year %in% c("2023", "202223", "2022/23", "2022/2023")  ~ "2021202223",
    Year %in% c("2024", "202324", "2023/24", "2023/2024")  ~ "2021202324",
    Year %in% c("2025", "202425", "2024/25", "2024/2025")  ~ "2021202425",
    Year %in% c("2026", "202526", "2025/26", "2025/2026")  ~ "2021202526")
  return(unique(pattern))
}